#!/usr/bin/env python3
"""
Auto-Round Quantization Script
Generated by auto_quant skill

Model: Qwen/Qwen3.5-2B
Scheme: W4A16
Method: RTN (iters=0)
Format: auto_round
Device: cuda (single GPU)
"""

import time
import json
import os
import sys
from pathlib import Path

# Track start time
start_time = time.time()
errors = []
solutions = []

RUNTIME_DIR = "/root/.openclaw/workspace/quantized/runs/Qwen_Qwen3.5-2B-W4A16"
OUTPUT_DIR = "/root/.openclaw/workspace/quantized/Qwen_Qwen3.5-2B-W4A16"
VENV_PY = "/root/.venv/bin/python"

# Ensure output directories exist
os.makedirs(RUNTIME_DIR, exist_ok=True)
os.makedirs(OUTPUT_DIR, exist_ok=True)

print(f"Runtime dir: {RUNTIME_DIR}")
print(f"Output dir: {OUTPUT_DIR}")

try:
    from auto_round import AutoRound

    # Configuration
    model_name_or_path = "Qwen/Qwen3.5-2B"
    scheme = "W4A16"
    iters = 0  # RTN mode
    nsamples = 128
    format_str = "auto_round"
    num_gpus = 1

    print(f"Loading model: {model_name_or_path}")
    print(f"Scheme: {scheme}")
    print(f"Iters: {iters} (RTN)")
    print(f"nsamples: {nsamples}")
    print(f"Format: {format_str}")
    print(f"Device: cuda")

    # Create AutoRound instance - single GPU: device="cuda"
    # disable_opt_rtn=True avoids meta device offloading issue with MLLM models
    ar = AutoRound(
        model_name_or_path,
        scheme=scheme,
        iters=iters,
        nsamples=nsamples,
        device="cuda",
        disable_opt_rtn=True,
    )

    # Quantize and save
    print("Starting quantization...")
    ar.quantize_and_save(output_dir=OUTPUT_DIR, format=format_str)

    print(f"Quantization complete! Output: {OUTPUT_DIR}")

except Exception as e:
    errors.append(str(e))
    import traceback
    errors.append(traceback.format_exc())
    print(f"Error during quantization: {e}")
    traceback.print_exc()

finally:
    end_time = time.time()
    duration = end_time - start_time

    # Collect output files
    output_path = Path(RUNTIME_DIR)
    files_info = []
    if output_path.exists():
        for f in sorted(output_path.rglob("*")):
            if f.is_file() and not f.name.endswith(('.pyc', '.pyo', '__pycache__')):
                size = f.stat().st_size
                files_info.append(str(f))

    # Check quantized model dir too
    quant_path = Path(OUTPUT_DIR)
    quant_files = []
    if quant_path.exists():
        for f in sorted(quant_path.rglob("*")):
            if f.is_file() and not f.name.endswith(('.pyc', '.pyo', '__pycache__')):
                quant_files.append(str(f))

    # Calculate sizes
    original_size_mb = None
    quantized_size_mb = None
    compression_ratio = None

    # Try to get original model size
    try:
        import subprocess
        result = subprocess.run(
            [VENV_PY, "-c", 
             f"from huggingface_hub import snapshot_download; "
             f"path = snapshot_download('{model_name_or_path}'); "
             f"import os; total=sum(os.path.getsize(os.path.join(dp,f)) for dp,dn,fn in os.walk(path) for f in fn); print(total/1024/1024)"],
            capture_output=True, text=True, timeout=60
        )
        if result.returncode == 0:
            original_size_mb = float(result.stdout.strip())
    except:
        pass

    # Quantized size
    if quant_files:
        try:
            total = 0
            for f in quant_files:
                total += Path(f).stat().st_size
            quantized_size_mb = total / 1024 / 1024
            if original_size_mb:
                compression_ratio = original_size_mb / quantized_size_mb
        except:
            pass

    # Build summary
    summary = {
        "model_id": "Qwen/Qwen3.5-2B",
        "scheme": "W4A16",
        "method": "RTN",
        "export_format": "auto_round",
        "device": "cuda",
        "quant_num_gpus": "1",
        "num_gpus": "1",
        "output_dir": OUTPUT_DIR,
        "runtime_output_dir": RUNTIME_DIR,
        "quantized_model_dir": OUTPUT_DIR,
        "status": "failed" if errors else "success",
        "duration_seconds": duration,
        "original_size_mb": original_size_mb,
        "quantized_size_mb": quantized_size_mb,
        "compression_ratio": compression_ratio,
        "errors": errors,
        "solutions": solutions,
        "output_files": files_info + quant_files
    }

    summary_path = Path(RUNTIME_DIR) / "quant_summary.json"
    with open(summary_path, "w") as f:
        json.dump(summary, f, indent=2)

    print(f"\nSummary written to: {summary_path}")
    print(f"Status: {summary['status']}")
    print(f"Duration: {duration:.2f}s")
    if errors:
        print(f"Errors: {errors}")
