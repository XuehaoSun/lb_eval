#!/usr/bin/env python3
"""
Auto-Round Model Quantization Skill

使用 Intel Auto-Round 代码库对大模型进行量化。
"""

import os
import sys
import shutil
import argparse
from pathlib import Path


def create_venv_and_install(output_dir: str, auto_round_source: str = "/storage/lkk/auto-round"):
    """创建虚拟环境并安装 auto-round"""
    venv_path = os.path.join(output_dir, "venv")
    
    print(f"Creating virtual environment at {venv_path}...")
    
    # 创建虚拟环境
    os.system(f"python3 -m venv {venv_path}")
    
    # 激活虚拟环境并安装 auto-round
    pip_path = os.path.join(venv_path, "bin", "pip")
    
    print(f"Installing auto-round from {auto_round_source}...")
    
    # 拷贝源代码
    work_dir = os.path.join(output_dir, "auto-round-src")
    if os.path.exists(work_dir):
        shutil.rmtree(work_dir)
    
    print(f"Copying auto-round source to {work_dir}...")
    shutil.copytree(auto_round_source, work_dir)
    
    # 安装依赖和 auto-round
    os.system(f"{pip_path} install -U pip setuptools wheel")
    os.system(f"{pip_path} install -e {work_dir}")
    
    return venv_path, work_dir


def generate_quantize_script(
    model_path: str,
    output_dir: str,
    quant_type: str = "MXFP4",
    iters: int = 200,
    nsamples: int = 128,
    format: str = "auto_round",
    **kwargs
) -> str:
    """生成量化脚本"""
    
    # 映射量化类型
    scheme_map = {
        "mxfp4": "MXFP4",
        "mxfp8": "MXFP8", 
        "int4": "W4A16",
        "w4a16": "W4A16",
        "w8a16": "W8A16",
        "w3a16": "W3A16",
        "w2a16": "W2A16",
        "nvfp4": "NVFP4",
    }
    
    scheme = scheme_map.get(quant_type.lower(), quant_type.upper())
    
    script = f'''#!/usr/bin/env python3
"""
Auto-Round Quantization Script
Generated by auto_quant skill

Model: {model_path}
Output: {output_dir}
Scheme: {scheme}
Iterations: {iters}
Samples: {nsamples}
Format: {format}
"""

from auto_round import AutoRound

# 量化参数
model_name_or_path = "{model_path}"
output_dir = "{output_dir}"
scheme = "{scheme}"
iters = {iters}
nsamples = {nsamples}

print(f"Loading model: {{model_name_or_path}}")
print(f"Quantization scheme: {{scheme}}")
print(f"Iterations: {{iters}}")
print(f"Samples: {{nsamples}}")

# 创建 AutoRound 对象并量化
ar = AutoRound(
    model_name_or_path,
    scheme=scheme,
    iters=iters,
    nsamples=nsamples,
)

# 量化并保存
print(f"Starting quantization...")
ar.quantize_and_save(output_dir=output_dir, format="{format}")

print(f"Quantization complete! Model saved to: {{output_dir}}")
'''
    
    return script


def run_quantization(
    model_path: str,
    output_dir: str,
    quant_type: str = "MXFP4",
    iters: int = 200,
    nsamples: int = 128,
    format: str = "auto_round",
    auto_round_source: str = "/storage/lkk/auto-round",
    **kwargs
):
    """执行量化流程"""
    
    # 创建输出目录
    os.makedirs(output_dir, exist_ok=True)
    
    print("=" * 60)
    print("Auto-Round Model Quantization")
    print("=" * 60)
    print(f"Model: {model_path}")
    print(f"Output: {output_dir}")
    print(f"Quant Type: {quant_type}")
    print(f"Iters: {iters}")
    print(f"Samples: {nsamples}")
    print(f"Format: {format}")
    print("=" * 60)
    
    # Step 1: 创建虚拟环境并安装
    venv_path, work_dir = create_venv_and_install(output_dir, auto_round_source)
    
    # Step 2: 生成量化脚本
    script_content = generate_quantize_script(
        model_path=model_path,
        output_dir=output_dir,
        quant_type=quant_type,
        iters=iters,
        nsamples=nsamples,
        format=format,
        **kwargs
    )
    
    script_path = os.path.join(output_dir, "quantize_script.py")
    with open(script_path, "w") as f:
        f.write(script_content)
    
    print(f"Quantization script saved to: {script_path}")
    
    # Step 3: 执行量化脚本
    python_path = os.path.join(venv_path, "bin", "python")
    
    print("Starting quantization (this may take a while)...")
    print("-" * 60)
    
    result = os.system(f"{python_path} {script_path}")
    
    if result != 0:
        print("ERROR: Quantization failed!")
        return False
    
    print("-" * 60)
    print("Quantization completed successfully!")
    
    return True


def main():
    parser = argparse.ArgumentParser(description="Auto-Round Model Quantization")
    parser.add_argument("--model", required=True, help="Model path or HuggingFace ID")
    parser.add_argument("--output", required=True, help="Output directory")
    parser.add_argument("--quant-type", default="MXFP4", help="Quantization type (mxfp4, mxfp8, int4, w4a16, etc.)")
    parser.add_argument("--iters", type=int, default=200, help="Number of iterations")
    parser.add_argument("--nsamples", type=int, default=128, help="Number of calibration samples")
    parser.add_argument("--format", default="auto_round", help="Export format")
    parser.add_argument("--source", default="/storage/lkk/auto-round", help="Auto-round source directory")
    
    args = parser.parse_args()
    
    run_quantization(
        model_path=args.model,
        output_dir=args.output,
        quant_type=args.quant_type,
        iters=args.iters,
        nsamples=args.nsamples,
        format=args.format,
        auto_round_source=args.source,
    )


if __name__ == "__main__":
    main()
